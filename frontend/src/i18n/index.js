export function useT(lang) {
  return (key) => {
    const val = I18N[lang]?.[key] ?? I18N.fr?.[key]
    if (val === undefined) {
      if (import.meta.env.DEV) console.warn(`[i18n] missing key: "${key}" (lang: ${lang})`)
      return key
    }
    return val
  }
}

export default I18N
